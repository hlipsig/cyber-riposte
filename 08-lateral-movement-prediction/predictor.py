#!/usr/bin/env python3
"""
Sketch: predict lateral movement targets from a compromised host.

The agent reasons over network topology, credential sharing, trust
relationships, and service adjacency to predict where an attacker
will pivot next — then PRs firewall rules to cut those paths.
"""

import subprocess
import yaml
from pathlib import Path

TEMPLATE_DIR = Path(__file__).parent


def load_topology(path="topology-example.yaml"):
    with open(TEMPLATE_DIR / path) as f:
        return yaml.safe_load(f)


def predict_pivot_targets(compromised_host, topology):
    """
    Score potential pivot targets by accessibility from the compromised host.

    Factors:
    - Same subnet (easy ARP/scan)
    - Shared credentials (SSH keys, service accounts)
    - Trust relationships (SSH known_hosts, NFS mounts)
    - Direct service access (database ports, admin panels)
    """
    targets = []
    compromised = topology["hosts"].get(compromised_host, {})
    comp_subnet = compromised.get("subnet", "")
    comp_keys = set(compromised.get("ssh_keys", []))
    comp_mounts = set(compromised.get("nfs_mounts", []))
    comp_trusts = set(compromised.get("trusts", []))
    comp_db_access = set(compromised.get("db_access", []))

    for hostname, info in topology["hosts"].items():
        if hostname == compromised_host:
            continue

        reasons = []
        score = 0

        if info.get("subnet") == comp_subnet:
            reasons.append("Same subnet")
            score += 3

        shared_keys = comp_keys & set(info.get("ssh_keys", []))
        if shared_keys:
            reasons.append(f"Shared SSH key: {', '.join(shared_keys)}")
            score += 5

        if hostname in comp_trusts:
            reasons.append("SSH trust relationship")
            score += 4

        if hostname in comp_db_access:
            reasons.append("Direct DB access via app credentials")
            score += 4

        host_exports = set(info.get("nfs_exports", []))
        if comp_mounts & host_exports:
            reasons.append("Mounted shared filesystem")
            score += 3

        if score > 0:
            targets.append({
                "hostname": hostname,
                "ip": info.get("ip", "unknown"),
                "score": score,
                "reasons": reasons,
            })

    targets.sort(key=lambda t: t["score"], reverse=True)
    return targets


def generate_isolation_rules(compromised_ip, targets):
    """Generate nftables rules to cut predicted pivot paths."""
    rules = [
        f"# Auto-generated by cyber-riposte agent",
        f"# Preemptive isolation of pivot paths from {compromised_ip}",
        f"table inet filter {{",
        f"    chain forward {{",
    ]
    for t in targets:
        rules.append(
            f'        ip saddr {compromised_ip} ip daddr {t["ip"]} '
            f'counter drop comment "cyber-riposte: block pivot to {t["hostname"]}"'
        )
    rules += [
        f"    }}",
        f"}}",
    ]
    return "\n".join(rules)


def open_pr(compromised_host, compromised_ip, targets):
    branch = f"riposte/isolate-{compromised_host}"
    rules = generate_isolation_rules(compromised_ip, targets)
    rule_file = TEMPLATE_DIR / f"isolate-{compromised_host}.nft"
    rule_file.write_text(rules + "\n")

    target_table = "\n".join(
        f"| `{t['hostname']}` ({t['ip']}) | {', '.join(t['reasons'])} |"
        for t in targets
    )

    subprocess.run(["git", "checkout", "-b", branch], check=True)
    subprocess.run(["git", "add", str(rule_file)], check=True)
    subprocess.run(
        ["git", "commit", "-m",
         f"predict: preemptive isolation — {len(targets)} likely pivot targets from {compromised_host}"],
        check=True,
    )
    subprocess.run(["git", "push", "-u", "origin", branch], check=True)
    subprocess.run(
        [
            "gh", "pr", "create",
            "--title", f"predict: preemptive isolation — {len(targets)} likely pivot targets from {compromised_host}",
            "--body", (
                f"Host `{compromised_host}` ({compromised_ip}) confirmed compromised.\n\n"
                f"**Predicted pivot targets:**\n\n"
                f"| Target | Reason |\n|---|---|\n{target_table}\n\n"
                f"Proposing preemptive firewall rules to cut these paths.\n\n"
                f"---\n*Opened by cyber-riposte agent*"
            ),
            "--reviewer", "security-team",
        ],
        check=True,
    )
    subprocess.run(["git", "checkout", "main"], check=True)
